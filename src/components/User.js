import React from "react"
import { IoCloseCircleSharp, IoHammerSharp, } from 'react-icons/io5'
import { FaAccessibleIcon } from "react-icons/fa";
import AddUser from "./AddUser";

// ‚úÖ –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –≤—Å–µ 5 –∫–∞—Ä—Ç–∏–Ω–æ–∫ –¥–ª—è —Ä–µ–∑–µ—Ä–≤–Ω–æ–≥–æ –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è
import userImage1 from '../img/img_1.jpeg'
import userImage2 from '../img/img_2.jpeg'
import userImage3 from '../img/img_3.jpeg'
import userImage4 from '../img/img_4.jpeg'
import userImage5 from '../img/img_5.jpeg'

class User extends React.Component {
	constructor( props ) {
		super( props )
		// –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è —Å–æ—Å—Ç–æ—è–Ω–∏—è –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–∞
		this.state = {
			editForm: false,    // –§–ª–∞–≥ –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è/—Å–∫—Ä—ã—Ç–∏—è —Ñ–æ—Ä–º—ã —Ä–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
			showImage: false,   // –§–ª–∞–≥ –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è/—Å–∫—Ä—ã—Ç–∏—è –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–æ–≥–æ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è
			avatarUrl: ""       // URL —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω–æ–≥–æ –∞–≤–∞—Ç–∞—Ä–∞
		}

		// ‚úÖ –ú–∞—Å—Å–∏–≤ —Å–æ –≤—Å–µ–º–∏ –∑–∞–ø–∞—Å–Ω—ã–º–∏ –∫–∞—Ä—Ç–∏–Ω–∫–∞–º–∏
		this.userImages = [userImage1, userImage2, userImage3, userImage4, userImage5]

		// ‚úÖ –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º —Å–ª—É—á–∞–π–Ω—É—é –∫–∞—Ä—Ç–∏–Ω–∫—É –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–∞ –æ—Å–Ω–æ–≤–µ –µ–≥–æ ID
		// –≠—Ç–æ –æ–±–µ—Å–ø–µ—á–∏–≤–∞–µ—Ç –ø–æ—Å—Ç–æ—è–Ω—Å—Ç–≤–æ - –æ–¥–∏–Ω –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤—Å–µ–≥–¥–∞ –ø–æ–ª—É—á–∞–µ—Ç –æ–¥–Ω—É –∏ —Ç—É –∂–µ –∫–∞—Ä—Ç–∏–Ω–∫—É
		this.userRandomImage = this.getRandomImageForUser()
	}

	// –°–æ—Ö—Ä–∞–Ω—è–µ–º –ø–µ—Ä–µ–¥–∞–Ω–Ω–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤ —Å–≤–æ–π—Å—Ç–≤–æ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–∞
	user = this.props.user

	// ‚úÖ –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –¥–µ—Ç–µ—Ä–º–∏–Ω–∏—Ä–æ–≤–∞–Ω–Ω–æ–π —Å–ª—É—á–∞–π–Ω–æ–π –∫–∞—Ä—Ç–∏–Ω–∫–∏ –Ω–∞ –æ—Å–Ω–æ–≤–µ ID –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
	getRandomImageForUser = () => {
		const randomIndex = this.user.id % this.userImages.length // –ò—Å–ø–æ–ª—å–∑—É–µ–º –æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è –¥–ª—è —Ä–∞–≤–Ω–æ–º–µ—Ä–Ω–æ–≥–æ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è
		return this.userImages[randomIndex]
	}

	// ‚úÖ –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ –∞–≤–∞—Ç–∞—Ä–∞ –Ω–∞ –æ—Å–Ω–æ–≤–µ –∏–º–µ–Ω–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —á–µ—Ä–µ–∑ DiceBear API
	generateAvatar = () => {
		// –î–æ—Å—Ç—É–ø–Ω—ã–µ —Å—Ç–∏–ª–∏ –∞–≤–∞—Ç–∞—Ä–æ–≤ –∏–∑ –±–∏–±–ª–∏–æ—Ç–µ–∫–∏ DiceBear
		const avatarStyles = [
			'avataaars', 'micah', 'adventurer', 'big-ears', 'bottts',
			'fun-emoji', 'lorelei', 'notionists', 'open-peeps'
		]

		// –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—ã–π —Å—Ç–∏–ª—å –∏–∑ –¥–æ—Å—Ç—É–ø–Ω—ã—Ö
		const randomStyle = avatarStyles[Math.floor( Math.random() * avatarStyles.length )]

		// –ò—Å–ø–æ–ª—å–∑—É–µ–º –∏–º—è, username –∏–ª–∏ 'user' –∫–∞–∫ seed –¥–ª—è –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ –ø–æ—Å—Ç–æ—è–Ω–Ω–æ–≥–æ –∞–≤–∞—Ç–∞—Ä–∞
		const seed = this.user.name || this.user.username || 'user'

		// –§–æ—Ä–º–∏—Ä—É–µ–º URL –¥–ª—è API DiceBear
		return `https://api.dicebear.com/7.x/${randomStyle}/svg?seed=${encodeURIComponent( seed )}`
	}

	componentDidMount() {
		// ‚úÖ –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –∞–≤–∞—Ç–∞—Ä –ø—Ä–∏ –º–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–∏–∏ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–∞
		this.setState( {
			avatarUrl: this.generateAvatar()
		} )
	}

	// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–∫–∞–∑–∞/—Å–∫—Ä—ã—Ç–∏—è –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–æ–≥–æ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
	toggleImage = () => {
		this.setState( {
			showImage: !this.state.showImage
		} )
	}

	render() {
		return (
			<div className="user">
				{/* –ò–∫–æ–Ω–∫–∞ —É–¥–∞–ª–µ–Ω–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è */}
				<IoCloseCircleSharp
					onClick={() => this.props.onDelete( this.user.id )}
					className="delite-icon"
				/>

				{/* –ò–∫–æ–Ω–∫–∞ —Ä–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è - –ø–µ—Ä–µ–∫–ª—é—á–∞–µ—Ç —Ñ–æ—Ä–º—É —Ä–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏—è */}
				<IoHammerSharp
					onClick={() => {
						this.setState( {
							editForm: !this.state.editForm
						} )
					}}
					className="edit-icon"
				/>

				{/* –ò–∫–æ–Ω–∫–∞ –¥–ª—è –ø–æ–∫–∞–∑–∞/—Å–∫—Ä—ã—Ç–∏—è –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–æ–≥–æ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è */}
				<FaAccessibleIcon
					onClick={this.toggleImage}
					className="style-icon"
					title="–ü–æ–∫–∞–∑–∞—Ç—å/—Å–∫—Ä—ã—Ç—å –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ"
				/>

				{/* ‚úÖ –ë–ª–æ–∫ –∞–≤–∞—Ç–∞—Ä–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è */}
				<div className="user-avatar">
					<img
						src={this.state.avatarUrl} // –ò—Å–ø–æ–ª—å–∑—É–µ–º —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –∞–≤–∞—Ç–∞—Ä
						alt={`–ê–≤–∞—Ç–∞—Ä ${this.user.name}`}
						className="avatar-image"
						onError={( e ) => {
							// ‚úÖ –ó–∞–ø–∞—Å–Ω–æ–π –≤–∞—Ä–∏–∞–Ω—Ç: –µ—Å–ª–∏ –∞–≤–∞—Ç–∞—Ä –Ω–µ –∑–∞–≥—Ä—É–∑–∏–ª—Å—è, –∏—Å–ø–æ–ª—å–∑—É–µ–º –ª–æ–∫–∞–ª—å–Ω—É—é –∫–∞—Ä—Ç–∏–Ω–∫—É
							e.target.src = this.userRandomImage
						}}
					/>
				</div>

				{/* –û—Å–Ω–æ–≤–Ω–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ */}
				<h3>{this.user.name}</h3>
				<h3>{this.user.username}</h3>
				<p>{this.user.email}</p>

				{/* –ê–¥—Ä–µ—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å –ø—Ä–æ–≤–µ—Ä–∫–æ–π –Ω–∞ —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏–µ */}
				<p>üìç {this.user.address && `${this.user.address.street}, ${this.user.address.city}`}</p>

				{/* –î–µ—Ç–∞–ª–∏ –∞–¥—Ä–µ—Å–∞ (–æ—Ç–æ–±—Ä–∞–∂–∞—é—Ç—Å—è —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –µ—Å—Ç—å –¥–∞–Ω–Ω—ã–µ) */}
				{this.user.address && (
					<div className="address-details">
						<small>
							{this.user.address.suite && `–ö–≤–∞—Ä—Ç–∏—Ä–∞: ${this.user.address.suite}`}<br />
							{this.user.address.zipcode && `–ò–Ω–¥–µ–∫—Å: ${this.user.address.zipcode}`}
						</small>
					</div>
				)}

				{/* –°—Ç–∞—Ç—É—Å –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è */}
				<b>{this.user.isHappy ? '–°—á–∞—Å—Ç–ª–∏–≤ :)' : '–ù–µ –æ—Å–æ–±–æ —Å—á–∞—Å—Ç–ª–∏–≤ :('}</b>

				{/* –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–æ–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è (–ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç—Å—è –ø–æ –∫–ª–∏–∫—É) */}
				{this.state.showImage && (
					<div className="user-image-container">
						<img
							src={this.userRandomImage} // ‚úÖ –ò—Å–ø–æ–ª—å–∑—É–µ–º –¥–µ—Ç–µ—Ä–º–∏–Ω–∏—Ä–æ–≤–∞–Ω–Ω—É—é —Å–ª—É—á–∞–π–Ω—É—é –∫–∞—Ä—Ç–∏–Ω–∫—É
							alt={`${this.user.name} ${this.user.username}`}
							className="user-image"
							onError={( e ) => {
								// ‚úÖ –°–∫—Ä—ã–≤–∞–µ–º –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –µ—Å–ª–∏ –æ–Ω–æ –Ω–µ –∑–∞–≥—Ä—É–∑–∏–ª–æ—Å—å
								e.target.style.display = 'none'
							}}
						/>
						<button
							onClick={this.toggleImage}
							className="close-image-btn"
						>
							–ó–∞–∫—Ä—ã—Ç—å
						</button>
					</div>
				)}

				{/* –§–æ—Ä–º–∞ —Ä–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è (–ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç—Å—è –ø–æ –∫–ª–∏–∫—É –Ω–∞ –∏–∫–æ–Ω–∫—É —Ä–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏—è) */}
				{this.state.editForm && <AddUser user={this.user} onAdd={this.props.onEdit} />}
			</div>
		)
	}
}

export default User


