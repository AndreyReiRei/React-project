// –ü–æ—Ç–æ–∫ –¥–∞–Ω–Ω—ã—Ö:
// –ù–∏—Å—Ö–æ–¥—è—â–∏–π –ø–æ—Ç–æ–∫: App ‚Üí Users ‚Üí User (–¥–∞–Ω–Ω—ã–µ —á–µ—Ä–µ–∑ props)

// –í–æ—Å—Ö–æ–¥—è—â–∏–π –ø–æ—Ç–æ–∫: User ‚Üí Users ‚Üí App (–¥–µ–π—Å—Ç–≤–∏—è —á–µ—Ä–µ–∑ callbacks)

// üîÑ –ñ–∏–∑–Ω–µ–Ω–Ω—ã–π —Ü–∏–∫–ª:
// –ú–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ: constructor ‚Üí render ‚Üí API –∑–∞–ø—Ä–æ—Å ‚Üí setState

// –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ: –î–µ–π—Å—Ç–≤–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è ‚Üí –≤—ã–∑–æ–≤ –º–µ—Ç–æ–¥–æ–≤ ‚Üí setState ‚Üí –ø–µ—Ä–µ—Ä–µ–Ω–¥–µ—Ä